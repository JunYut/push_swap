#include "../../includes/push_swap.h"

void	sort(t_stack *A, t_stack *B)
{
console_log("sorting...");		// debug
	if (is_sorted(A, 1) && is_sorted(B, -1))
		return ;
	sort_stack_A(A, B);
	sort_stack_B(B, A);
	while (B->size > 0)
		pa(A, B);
}
